    // å¦‚æœæ²¡æœ‰é€‰æ‹©è§„åˆ™é›†ï¼Œæ˜¾ç¤ºæ‰€æœ‰é˜Ÿä¼
    return petStorageStore.teams
  }

  // è¿‡æ»¤å‡ºè§„åˆ™é›†åŒ¹é…çš„é˜Ÿä¼
  return petStorageStore.teams.filter(team => {
    const teamRuleSetId = team.ruleSetId || 'casual_standard_ruleset' // é»˜è®¤ä¸ºä¼‘é—²è§„åˆ™é›†
    return teamRuleSetId === selectedRuleSetId.value
