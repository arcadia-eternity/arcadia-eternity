const isMatchButtonDisabled = computed(() => {
  const currentState = battleClientStore.currentState
  const isNotConnected = currentState.status !== 'connected'
  const isSearching = currentState.matchmaking === 'searching'
  const isMatched = currentState.matchmaking === 'matched'
  const noRuleSelected = !selectedRuleSetId.value
  const noTeamSelected = selectedTeamIndex.value === -1
  const teamInvalid = !isSelectedTeamValid.value
  const teamIncompatible = !isSelectedTeamCompatible.value

  const disabled =
    isNotConnected || isSearching || isMatched || noRuleSelected || noTeamSelected || teamInvalid || teamIncompatible

  console.log(
    'ğŸ”’ isMatchButtonDisabled computed:',
    disabled,
    'notConnected:',
    isNotConnected,
    'searching:',
    isSearching,
    'matched:',
    isMatched,
    'noRuleSelected:',
    noRuleSelected,
    'noTeamSelected:',
    noTeamSelected,
    'teamInvalid:',
    teamInvalid,
    'teamIncompatible:',
    teamIncompatible,
  )
  return disabled
})

const errorMessage = ref<string | null>(null)

const handleMatchmaking = async () => {
  try {
    console.log('ğŸ® handleMatchmaking called, current isMatching:', isMatching.value)
    // æ¸…é™¤ä¹‹å‰çš„é”™è¯¯ä¿¡æ¯
    errorMessage.value = null

    // æ£€æŸ¥è¿æ¥çŠ¶æ€
    if (battleClientStore.currentState.status !== 'connected') {
      errorMessage.value = 'è¯·å…ˆè¿æ¥åˆ°æœåŠ¡å™¨'
      return
    }

    // æ£€æŸ¥è§„åˆ™é€‰æ‹©
    if (!selectedRuleSetId.value) {
      errorMessage.value = 'è¯·é€‰æ‹©æ¸¸æˆè§„åˆ™'
      return
    }

    // æ£€æŸ¥é˜Ÿä¼é€‰æ‹©
    if (selectedTeamIndex.value === -1 || !selectedTeam.value) {
      errorMessage.value = 'è¯·é€‰æ‹©é˜Ÿä¼'
      return
    }

    // æ£€æŸ¥é˜Ÿä¼æ˜¯å¦ç¬¦åˆè§„åˆ™
    if (!isSelectedTeamValid.value) {
      // ä½¿ç”¨å·²ç»è®¡ç®—å¥½çš„éªŒè¯é”™è¯¯ä¿¡æ¯
      if (selectedTeamValidationErrors.value.length > 0) {
        errorMessage.value = `é˜Ÿä¼ä¸ç¬¦åˆè§„åˆ™è¦æ±‚ï¼š${selectedTeamValidationErrors.value[0]}`
      } else {
        errorMessage.value = 'æ‰€é€‰é˜Ÿä¼ä¸ç¬¦åˆè§„åˆ™è¦æ±‚'
      }
      return
    }

    if (isMatching.value) {
      console.log('âŒ Canceling matchmaking')
      try {
        await battleClientStore.cancelMatchmaking()
      } catch (error) {
        console.warn('âš ï¸ Cancel matchmaking failed (probably already matched):', error)
        // å¦‚æœå–æ¶ˆå¤±è´¥ï¼Œå¯èƒ½æ˜¯å› ä¸ºå·²ç»åŒ¹é…æˆåŠŸäº†ï¼Œå¿½ç•¥é”™è¯¯
      }
    } else {
      console.log(
        'ğŸ” Starting matchmaking for player:',
        playerStore.player.id,
        'with rule:',
        selectedRuleSetId.value,
        'team:',
        selectedTeam.value.name,
      )

      // æ„å»ºç©å®¶æ•°æ®ï¼Œä½¿ç”¨é€‰å®šçš„é˜Ÿä¼
      const playerData = {
        ...playerStore.player,
        team: selectedTeam.value.pets,
      }

      // å‘é€åŒ¹é…è¯·æ±‚ï¼ŒåŒ…å«è§„åˆ™é›†ä¿¡æ¯
      await battleClientStore.joinMatchmaking({
        playerSchema: playerData,
        ruleSetId: selectedRuleSetId.value,
      })
      console.log('âœ… Matchmaking request sent with ruleSetId:', selectedRuleSetId.value)

      battleClientStore.once('matchSuccess', async () => {
        console.log('ğŸ¯ matchSuccess event received in lobbyPage')
        if (!battleClientStore._instance) {
          throw new Error('BattleClient instance not available')
        }
        console.log('ğŸ—ï¸ Initializing battle system')
        await battleStore.initBattle(
          new RemoteBattleSystem(battleClientStore._instance as BattleClient),
          playerStore.player.id,
        )
        const currentState = battleClientStore.currentState
        const roomId = currentState.roomId || ''
        console.log('ğŸš€ Navigating to battle page with roomId:', roomId, 'full state:', currentState)
        router.push({
          path: '/battle',
          query: { roomId },
        })
      })
    }
  } catch (error) {
    console.error('ğŸ’¥ Error in handleMatchmaking:', error)
    errorMessage.value = (error as Error).message
    setTimeout(() => (errorMessage.value = null), 3000)
  }
}
